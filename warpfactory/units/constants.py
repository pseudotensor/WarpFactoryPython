"""
Universal physical constants
"""

def c():
    """
    Speed of light in vacuum

    Returns:
        float: Speed of light in m/s
    """
    return 2.99792458e8  # m / s


def G():
    """
    Gravitational constant

    Returns:
        float: Gravitational constant in m^3 / kg / s^2
    """
    return 6.67430e-11  # m^3 / kg / s^2
